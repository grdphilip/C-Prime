/* 
In C++, as in most programming languages, the type of an object determines what operations can be performed on it. 
Some languages, such as Smalltalk and Python, check types at run time. In contrast, C++ is a statically typed language; 
type checking is done at compile time. As a consequence, the compiler must know the type of every name used in the program.

Perhaps the most important feature in C++ is the class, which lets programmers define their own types. 
In C++ such types are some- times called “class types” to distinguish them from the types that are built into the language. 

Types are fundamental to any program: They tell us what our data mean and what operations we can perform on those data.
Different types are specified at 2.1.1
*/

bool b = 42; // b is true
int i = b; // i has value 1
i = 3.14; // i has value 3
double pi = i; // pi has value 3.0
unsigned char c = .1; // assuming 8-bit chars, c has value 255
signed char c2 = 256; // assuming 8-bit chars, the value of c2 is undefined

